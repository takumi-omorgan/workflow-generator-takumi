#!/usr/bin/env python3
"""Build the GitHub publish plan from a review artifact (bin/publish-review).

Reads an ai-review-artifact JSON and, optionally, the PR diff (to re-validate
inline-comment line positions). Emits a JSON plan:

  {
    "pr": N,
    "artifactHash": "...",
    "topLevelBody": "<markdown review body>",
    "inlineComments": [ {"path","line","body"} ],
    "ghPayload": { "body","event":"COMMENT","comments":[...] },
    "counts": {"inline":K,"topLevel":M}
  }

Findings whose file/line are not in the diff (or that are not high-confidence,
or are praise) are folded into the top-level body, never posted inline.

Env: ARTIFACT (path, required), PR (optional override), DIFF_FILE (optional).
"""
import os
import sys
import json

import review_common as rc


def main():
    path = os.environ["ARTIFACT"]
    try:
        art = json.load(open(path, encoding="utf-8"))
    except Exception as e:
        sys.stderr.write("publish-review: artifact is not valid JSON: %s\n" % e)
        sys.exit(2)
    if art.get("schema") != "ai-review-artifact":
        sys.stderr.write("publish-review: %s is not an ai-review-artifact\n" % path)
        sys.exit(2)

    pr = os.environ.get("PR") or art.get("pr")
    pr = int(pr) if pr not in (None, "") else None

    findings = art.get("findings", [])

    # Optional re-validation against the live diff: a finding stays inline only
    # if its file+line are still present in the diff hunks.
    diff_file = os.environ.get("DIFF_FILE") or ""
    diff_lines = None
    if diff_file and os.path.isfile(diff_file):
        diff_lines = rc.parse_diff_lines(open(diff_file, encoding="utf-8").read())

    inline = []
    top_level = []
    for f in findings:
        commentable = bool(f.get("commentable"))
        if commentable and diff_lines is not None:
            commentable = bool(f.get("file") and f.get("line") is not None
                               and f["line"] in diff_lines.get(f["file"], set()))
        if commentable:
            inline.append(f)
        else:
            top_level.append(f)

    # ---- top-level body ----
    labels = {"blocking": "🛑 Blocking", "non-blocking": "💡 Non-blocking",
              "question": "❓ Question", "praise": "👍 Praise"}
    order = {"blocking": 0, "non-blocking": 1, "question": 2, "praise": 3}
    body = ["## AI review", "",
            "_Generated by the workflow kit's AI PR review (dry-run, then "
            "explicitly approved). Treat as a first pass, not a gate._", "",
            art.get("summary", "")]
    st = art.get("stats", {})
    body.append("")
    body.append("**Findings:** %d blocking · %d non-blocking · %d question · %d praise"
                % (st.get("blocking", 0), st.get("non-blocking", 0),
                   st.get("question", 0), st.get("praise", 0)))
    if inline:
        body.append("")
        body.append("_%d high-confidence finding(s) are attached as inline comments._" % len(inline))
    shown = sorted(top_level, key=lambda f: order.get(f["classification"], 9))
    current = None
    for f in shown:
        if f["classification"] != current:
            current = f["classification"]
            body.append("")
            body.append("### %s" % labels.get(current, current))
        loc = ""
        if f.get("file"):
            loc = " (`%s%s`)" % (f["file"], (":%d" % f["line"]) if f.get("line") else "")
        body.append("")
        body.append("- **%s**%s — %s" % (f["title"], loc, f.get("detail", "")))
        if f.get("suggestion"):
            body.append("  - _Suggested fix:_ %s" % f["suggestion"])
    top_level_body = "\n".join(body).rstrip() + "\n"

    # ---- inline comments + gh payload ----
    inline_comments = []
    for f in inline:
        text = "**%s** [%s/%s]\n\n%s" % (f["title"], f.get("severity", "low"),
                                         f.get("category", ""), f.get("detail", ""))
        if f.get("suggestion"):
            text += "\n\n_Suggested fix:_ %s" % f["suggestion"]
        inline_comments.append({"path": f["file"], "line": f["line"], "body": text})

    gh_payload = {
        "body": top_level_body,
        "event": "COMMENT",
        "comments": [{"path": c["path"], "line": c["line"], "side": "RIGHT",
                      "body": c["body"]} for c in inline_comments],
    }

    print(json.dumps({
        "pr": pr,
        "artifactHash": art.get("artifactHash"),
        "topLevelBody": top_level_body,
        "inlineComments": inline_comments,
        "ghPayload": gh_payload,
        "counts": {"inline": len(inline_comments), "topLevel": len(shown)},
    }, ensure_ascii=False))


if __name__ == "__main__":
    main()
